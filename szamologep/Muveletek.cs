using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace CalculatorByBalint
{
    class Muveletek
    {
        public double osszeadas(double szam1, double szam2)
        {
            return szam1 + szam2;
        }
        public double kivonas(double szam1, double szam2)
        {
            return szam1 - szam2;
        }
        public double szorzas(double szam1, double szam2)
        {
            return szam1 * szam2;
        }
        public double osztas(double szam1, double szam2)
        {
            return szam1 / szam2;
        }
    }
}
